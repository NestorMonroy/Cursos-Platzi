package org.nestor.javatests.roman_numerals;

public class InfoNumeral {
    private int number;
    private String info;

    public InfoNumeral(int number, String info) {
        this.number = number;
        this.info = info;
    }

    public int getNumber() {
        return number;
    }

    public String getInfo() {
        return info;
    }
}
